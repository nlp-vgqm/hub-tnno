#词典；每个词后方存储的是其词频，词频仅为示例，不会用到，也可自行修改
Dict = {"经常":0.1,
        "经":0.05,
        "有":0.1,
        "常":0.001,
        "有意见":0.1,
        "歧":0.001,
        "意见":0.2,
        "分歧":0.2,
        "见":0.05,
        "意":0.05,
        "见分歧":0.05,
        "分":0.1}

#待切分文本
sentence = "经常有意见分歧"

#实现全切分函数，输出根据字典能够切分出的所有的切分方式
def all_cut(sentence, Dict):
    target = []
    n = len(sentence)

    def backtrack(start, path):
        # 如果已经到达句子末尾，保存结果
        if start == n:
            target.append(path.copy())
            return

        # 尝试所有可能的词语长度
        for end in range(start + 1, n + 1):
            word = sentence[start:end]
            # 如果词语在字典中，继续递归
            if word in Dict:
                path.append(word)
                backtrack(end, path)
                path.pop()  # 回溯

    backtrack(0, [])
    return target

target = all_cut(sentence,Dict)
# print(target)
for i, seg in enumerate(target, 1):
    print(f"{i:2d}. {seg}")

